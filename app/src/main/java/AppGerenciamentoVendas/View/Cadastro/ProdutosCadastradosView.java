    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed

        if (tblProdutosCadastrados.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(this, "Selecione um cliente para editar", "ERRO", JOptionPane.WARNING_MESSAGE);
        } else {
            Long idProduto = (Long) tblProdutosCadastrados.getModel().getValueAt(tblProdutosCadastrados.getSelectedRow(), 0);
            CadastroProdutoView cadastroProduto = new CadastroProdutoView(null, true, idProduto);
            cadastroProduto.setVisible(true);
            carregarTabelaCadastro();
        }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed

        if (tblProdutosCadastrados.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(this, "Selecione um cliente para excluir", "ERRO", JOptionPane.WARNING_MESSAGE);
        } else {
            Long id = (Long) tblProdutosCadastrados.getModel().getValueAt(tblProdutosCadastrados.getSelectedRow(), 0);
            try {
                produtoService.excluirProduto(id);
            } catch (Exception ex) {
            }
            JOptionPane.showMessageDialog(this, "Produto excluido com Sucesso", "SUCESS", JOptionPane.INFORMATION_MESSAGE);
        }

        carregarTabelaCadastro();

    }//GEN-LAST:event_btnExcluirActionPerformed

