    public void carregarTabelaCadastro() {
        listaProduto = produtoService.carregarListaProdutos();

        Object[][] produtos = new Object[listaProduto.size()][Produto.class.getDeclaredFields().length];

        int i = 0;
        for (Produto produto : listaProduto) {
            produtos[i][0] = produto.getId();
            produtos[i][1] = produto.getDescricao();
            produtos[i][2] = produto.getPreco();
            i++;
        }
        DefaultTableModel dataModel = new DefaultTableModel(produtos, new Object[]{"Codigo", "Descricao", "Preco"}) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        tblProdutosCadastrados.setModel(dataModel);

    }

