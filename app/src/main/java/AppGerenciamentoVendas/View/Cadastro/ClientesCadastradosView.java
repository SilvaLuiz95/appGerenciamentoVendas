    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed

        if (tblClienteCadastrados.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(this, "Selecione um cliente para editar", "ERRO", JOptionPane.WARNING_MESSAGE);
        } else {
            Long idCliente = (Long) tblClienteCadastrados.getModel().getValueAt(tblClienteCadastrados.getSelectedRow(), 0);
            CadastroClienteView cadastroCliente = new CadastroClienteView(null, true, idCliente);
            cadastroCliente.setVisible(true);
            carregarTabelaCadastro();
        }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed

        if (tblClienteCadastrados.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(this, "Selecione um cliente para excluir", "ERRO", JOptionPane.WARNING_MESSAGE);
        } else {
            Long id = (Long) tblClienteCadastrados.getModel().getValueAt(tblClienteCadastrados.getSelectedRow(), 0);
            try {
                clienteService.excluirCliente(id);
            } catch (Exception ex) {
                Logger.getLogger(ClientesCadastradosView.class.getName()).log(Level.SEVERE, null, ex);
            }
            JOptionPane.showMessageDialog(this, "Cliente excluido com Sucesso", "SUCESS", JOptionPane.INFORMATION_MESSAGE);
        }

        carregarTabelaCadastro();
    }//GEN-LAST:event_btnExcluirActionPerformed

