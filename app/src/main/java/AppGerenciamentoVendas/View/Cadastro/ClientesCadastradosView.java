    public void carregarTabelaCadastro() {
        listaClientes = clienteService.carregarListaClientes();

        Object[][] clientes = new Object[listaClientes.size()][Cliente.class.getDeclaredFields().length];

        int i = 0;
        for (Cliente cliente : listaClientes) {
            clientes[i][0] = cliente.getId();
            clientes[i][1] = cliente.getNome();
            clientes[i][2] = cliente.getDiaFechamento();
            clientes[i][3] = cliente.getLimiteCompra();
            i++;
        }
        DefaultTableModel dataModel = new DefaultTableModel(clientes, new Object[]{"Codigo", "Nome", "Dia Fechamento", "Limite de Compra"}) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        tblClienteCadastrados.setModel(dataModel);
    }

